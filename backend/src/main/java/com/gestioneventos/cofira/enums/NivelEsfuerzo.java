package com.gestioneventos.cofira.enums;

/**
 * Represents the perceived effort level during an exercise.
 * Based on RPE (Rate of Perceived Exertion) concept.
 */
public enum NivelEsfuerzo {
    FACIL,          // Easy - could do many more reps
    MODERADO,       // Moderate - challenging but manageable
    DIFICIL,        // Hard - near maximum effort
    MUY_DIFICIL     // Very hard - at or near failure
}
